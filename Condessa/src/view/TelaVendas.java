    public void MostrarVenda(){
        String sql = "select ve.statusvenda, cl.nome, ve.id, iv.quantidade_comprada, iv.formadepagamento, iv.datavenda, iv.valor_compra, \n" +
                      "GROUP_CONCAT(pr.nome SEPARATOR ', ') as \"Nome dos produtos\",GROUP_CONCAT(iv.quantidade_comprada SEPARATOR ' | ') as \"Qtd_compra\"\n" +
                      "from item_venda iv\n" +
                      "inner join estoque es on es.id = iv.id_estoque \n" +
                      "inner join vendas ve on ve.id = iv.id_vendas \n" +
                      "inner join cliente cl on cl.id = iv.id_cliente\n" +
                      "inner join produto pr on pr.id = es.ID_produto\n" +
                      "where iv.id_vendas = ve.id\n" +
                      "GROUP BY iv.id_vendas";
        try {
           pst = conexao.prepareStatement(sql);
           rs = pst.executeQuery(sql);
           DefaultTableModel model =(DefaultTableModel) TabelaVendas.getModel();
           model.setNumRows(0);
           while(rs.next()){
              model.addRow(new Object[]
              {
                  rs.getInt("ve.id"),
                  rs.getString("iv.formadepagamento"),
                  rs.getDouble("iv.valor_compra"),
                  rs.getString("cl.nome"),
                  rs.getString("iv.datavenda"),
                  rs.getString("ve.statusvenda"),
                  rs.getString("Nome dos produtos"),
                  rs.getString("Qtd_compra"),
              });
          }
         pst.close();
        
        }
        catch (SQLException ex) {
            Logger.getLogger(TelaFornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
