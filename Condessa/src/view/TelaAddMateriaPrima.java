    public void listarMp(){
        String sql="select nome from fornecedor";
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            while(rs.next()){
                Forn_id.addItem(rs.getString("nome"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaAddMateriaPrima.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public int pegarId() throws SQLException{
        String sql = "select id from fornecedor where nome = '" + Forn_id.getSelectedItem().toString() + "'";
        try{
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            while(rs.next()){
                return rs.getInt("id");
            }
            
        }
        catch(SQLException ex){
            Logger.getLogger(TelaAddMateriaPrima.class.getName()).log(Level.SEVERE, null, ex);
        }
        return 0;
    }

