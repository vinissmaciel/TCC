    private void adicionar() throws SQLException{
        int id = pegarId();
        String sql = "insert into materiaprima"+
                     "(nome, quantidade,preco,id_fornecedor)"+
                     //? = n sei os dados q vao vim
                     "values (?,?,?,?)";
        try {
            //preparando o banco pra ser usado
            pst = conexao.prepareStatement(sql);
            //setando(colocando) os dados no banco
            pst.setString(1, Nome_mat.getText());
            pst.setString(2, Qtd_mat.getText());
            pst.setDouble(3, Double.parseDouble(jTextField1.getText()));
            pst.setInt(4, id);
            //executando o banco
            pst.executeUpdate();
            //mensagem para o usuario
            JOptionPane.showMessageDialog(null, "Materia-prima Cadastrada");
            //fechando a conexao e o banco
            pst.close();
            conexao.close();
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    

