    public void listarCliente(){
        String sql="select nome from cliente";
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            while(rs.next()){
                ex_cli.addItem(rs.getString("nome"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "erro listar cliente");
            Logger.getLogger(TelaAddVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
  
    
    public int pegarIdCliente() throws SQLException{
        String sql = "select id from cliente where nome = '" + ex_cli.getSelectedItem().toString() + "'";
        try{
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            while(rs.next()){
                return rs.getInt("id");
            }
            
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "erro pegar id cliente");
            Logger.getLogger(TelaAddVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
        return 0;
    }
    
    public void MostrarClientes() throws SQLException{
        String sql = "select * from cliente where id = '" + pegarIdCliente() + "'";
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            while(rs.next()){
                ex_no.setText(rs.getString("nome"));
                ex_tel.setText(rs.getString("telefone"));
                ex_cpf.setText(rs.getString("cpf"));
                ex_data.setText(rs.getString("DataDeNascimento"));
                ex_num.setText(""+rs.getInt("numero"));
                ed_Rua_cli.setText(rs.getString("rua"));
                ed_Cid_cli.setText(rs.getString("cidade"));
                ed_Bai_cli.setText(rs.getString("bairro"));
                ex_cep.setText(""+rs.getInt("cep"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaAddVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void DeleteCliente() throws SQLException{
        String sql = "delete from cliente where id = '" + pegarIdCliente() + "'";
        try {
            pst = conexao.prepareStatement(sql);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Cliente excluido com sucesso");
            pst.close();
            conexao.close();
            this.dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Cliente não pode ser excluído");
            Logger.getLogger(TelaAddVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
